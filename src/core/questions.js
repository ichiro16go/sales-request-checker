const QUESTION_BY_LABEL = {
  "現状（As-Is）": "現在どのような状態・動作になっていますか？スクリーンショットや画面名があると開発側が把握しやすくなります。",
  "発生条件": "この問題・要望はいつ、どのような状況で発生しますか？再現条件や操作手順を教えてください。",
  "対象範囲": "対象はどのユーザー・店舗・期間ですか？全体なのか、特定条件だけなのかを教えてください。",
  "背景・理由": "なぜ今この対応が必要ですか？問い合わせ増加、業務影響、契約上の事情などがあれば教えてください。",
  "変更後（To-Be）": "対応後にどのような状態になっていてほしいですか？画面表示・業務フロー・条件を具体化してください。",
  "ゴール・期待成果": "この対応によってどのような効果や改善を期待していますか？",
  "対応期限": "希望時期を具体的に教えてください。例: 6月中、6月30日まで、次回リリースまで。",
  "対象サービス・機能": "どのサービス・画面・機能についての依頼ですか？",
  "依頼概要（何をしたいか）": "今回、開発チームに何をしてほしいですか？変更・追加・修正したい内容を一文で教えてください。",
  "優先度": "他の依頼と比べた優先度や、急ぐ理由があれば教えてください。",
};

export function buildQuestions(missingItems) {
  return missingItems
    .map((label) => ({ label, question: QUESTION_BY_LABEL[label] }))
    .filter((row) => row.question);
}

export function buildFallbackImprovedDescription(snapshot, questions) {
  const lines = [
    "## 依頼概要",
    snapshot.summary || "（未記入）",
    "",
    "## 現状・背景",
    "（現状、発生条件、対象範囲、背景を追記してください）",
    "",
    "## 変更後の期待状態",
    "（対応後にどうなっていてほしいかを追記してください）",
    "",
    "## 確認したいこと",
    ...questions.map((row, index) => `${index + 1}. ${row.question}`),
  ];
  return lines.join("\n");
}
