export function buildQuestions(missingItems) {
  return missingItems
    .map((label) => ({ label, question: QUESTION_BY_LABEL[label] }))
    .filter((row) => row.question);
}

export function buildFallbackImprovedDescription(snapshot, questions, requestType) {
  if (requestType === "data-extraction") {
    const lines = [
      "## 抽出依頼概要",
      snapshot.summary || "（未記入）",
      "",
      "## 抽出条件",
      "（対象テーブル・ステータス・属性など、絞り込み条件を記載してください）",
      "",
      "## 対象期間",
      "（例: 2026年1月〜3月）",
      "",
      "## 出力形式・必要カラム",
      "（例: CSV形式、カラム: ユーザーID, 店舗名, 予約日時, ステータス）",
      "",
      "## 希望納期・受渡方法",
      "（例: 6月20日まで、Google Driveで共有）",
      "",
      "## 確認したいこと",
      ...questions.map((row, index) => `${index + 1}. ${row.question}`),
    ];
    return lines.join("\n");
  }
  const lines = [
    "## 依頼概要",
    snapshot.summary || "（未記入）",
    "",
    "## 現状・背景",
    "（現状、発生条件、対象範囲、背景を追記してください）",
    "",
    "## 変更後の期待状態",
    "（対応後にどうなっていてほしいかを追記してください）",
    "",
    "## 確認したいこと",
    ...questions.map((row, index) => `${index + 1}. ${row.question}`),
  ];
  return lines.join("\n");
}
